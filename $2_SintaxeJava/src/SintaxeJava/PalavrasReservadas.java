package SintaxeJava;

public class PalavrasReservadas {

	public static void main(String[] args) {

		/**
		 * O Java possui um conjunto de palavras que indicam os nomes dos tipos primitivos
		 * e outros elementos pertencentes à sintaxe dessa linguagem. Essas palavras são
		 * reservadas e não podem ser utilizadas com outro fim.
		 * 
		 */
		String palavrasReservadas = """
		/**
		 * Palavras reservadas
		 * abstract		assert		boolean		break		byte		case
		 * catch		char		class		continue	default		do
		 * double		else		enum		extends		false		final
		 * finally		float		for			if			implements	import
		 * instanceof	int			interface	long		native		new
		 * null			package		private		protected	public		return
		 * short		static		strictfp	super		switch		synchronized
		 * this			throw		throws		transient	true		try
		 * var			void		volatile	while		yield
		 * 
		 */
		 """;
		System.out.println(palavrasReservadas);

	}

}
